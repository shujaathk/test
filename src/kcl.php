$.getJSON('http://163.180.116.57:8080/dcl/lifelog/schema', function(data) {

var items = [];
  $.each( data, function( key, val ) {
    items.push( "<li id='" + key + "'>" + val + "</li>" );
  });
 
  $( "<ul/>", {
    "class": "my-new-list",
    html: items.join( "" )
  }).appendTo( "body" );    //data is the JSON string
});
